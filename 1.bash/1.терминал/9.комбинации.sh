# В Linux есть операторы для выполнения нескольких команд:
# |    - передаёт вывод одной команды на вход другой
# ||   - выполняет следующую команду, только если предыдущая завершилась с ошибкой
# &&   - выполняет следующую команду, только если предыдущая завершилась успешно
# ;    - выполняет команды последовательно, независимо от результата

# | (pipe) — передаёт вывод одной команды на вход другой.
# Пример: найти слово "error" в выводе команды ls
ls | grep "error"

# Пример: передать текст в файл через команду echo и подсчитать строки
echo -e "line1\nline2" | wc -l  # выведет количество строк: 2

# || — если первая команда завершилась с ошибкой, выполнит вторую.
# Пример: если файла нет, создать его
cat file.txt || touch file.txt
# Если файла file.txt нет, то команда cat завершится с ошибкой, и выполнится touch file.txt.

# && — выполнит следующую команду только если первая завершилась успешно.
# Пример: создать файл и сразу записать в него текст
touch new_file.txt && echo "Hello!" > new_file.txt
# Если файл успешно создастся, текст "Hello!" запишется в него.

# ; — просто выполняет команды последовательно, независимо от результата.
# Пример: выполнить две команды одну за другой
echo "First command" ; echo "Second command"
# Обе команды выполнятся независимо друг от друга.